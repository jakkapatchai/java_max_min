class x {
  public static void main(String args[]) {
    int c1 = Integer.parseInt(args[0]);
    int c2 = Integer.parseInt(args[1]);
    int c3 = Integer.parseInt(args[2]);
    int c4 = Integer.parseInt(args[3]);
    System.out.println("c1" + " + " + "c2" + " + " + "c3" + " + " + "c4" + " = " + (c1 + c2 + c3 + c4));
    System.out.println("c1" + " - " + "c2" + " - " + "c3" + " - " + "c4" + " = " + (c1 - c2 - c3 - c4));
    System.out.println("c1" + " * " + "c2" + " * " + "c3" + " * " + "c4" + " = " + (c1 * c2 * c3 * c4));
    System.out.println("c1" + " / " + "c2" + " / " + "c3" + " / " + "c4" + " = " + (c1 / c2 / c3 / c4));
  }
}